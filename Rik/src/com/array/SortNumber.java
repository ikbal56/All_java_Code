package com.array;

public class SortNumber {
	
	public static void main(String[] args) {
		
		int num1=199;
		int num2=8989;
		System.out.println(num1);
		System.out.println(num2);
		
		int temp;
		temp=num1;
		num1=num2;
		num2=temp;
		
		System.out.println(num1);
		System.out.println(num2);
		
	}

}
